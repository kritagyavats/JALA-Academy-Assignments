//Ques 4) Define variables for different data types int, boolean, char, float, double and print on the console.

package A01_Java_Basics;

public class Ques04 {
    public static void main(String[] args) {
        int i = 39;
        boolean b = false;
        char c = '@';
        float f = 44.2f;
        double d = 72.47693;

        System.out.println("Integer Datatype: "+ i);
        System.out.println("Boolean Datatype: "+ b);
        System.out.println("Char Datatype: "+ c);
        System.out.println("Float Datatype: "+ f);
        System.out.println("Double Datatype: "+ d);
    }
}
